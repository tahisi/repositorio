package processinter;

public class JsonDescarga {
	
	String Cadena="";
	String Nombre="";
	String Extension="";
	String Error="";

	public JsonDescarga(String cadena, String nombre,String extension,String error){
		
		this.Cadena     = cadena;
		this.Nombre     = nombre;
		this.Extension  = extension;
		this.Error      = error;
		
	}
	
	

}
