package com.syc.bancomer.bean;

public class beanRespuesta {
	String folioDigitalizacion;
	String codigo;
	String mensaje;
	String causa;
	public String getFolioDigitalizacion() {
		return folioDigitalizacion;
	}
	public void setFolioDigitalizacion(String folioDigitalizacion) {
		this.folioDigitalizacion = folioDigitalizacion;
	}
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getMensaje() {
		return mensaje;
	}
	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}
	public String getCausa() {
		return causa;
	}
	public void setCausa(String causa) {
		this.causa = causa;
	}

}
